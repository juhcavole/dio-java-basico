public class Operadores {
    
    public static void main(String[] args){

        //unários
        int numero = 5;
        boolean variavel = true;

        numero = -numero;

        System.out.println(numero);

        numero = numero * -1;

        System.out.println(numero);

        //incremento
        
        System.out.println(++numero);

        //decremento
        System.out.println(--numero);

        //Negação
        System.out.println(!variavel);

        
    }
}
